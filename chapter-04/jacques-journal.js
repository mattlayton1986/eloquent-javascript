/*
  Jacques' Journal -- a tale of a man turned weresquirrel
  *** This is a textbook example, not a project/assignment ***

  Jacques, having turned into a weresquirrel at night with no idea how it happens, decided to keep a journal of the things he did each day and whether he turned into a squirrel that day or not. He hoped doing so would provide a correlation between something he did (or didn't do) and his transformations. This project evaluates Jacques' journal to determine what correlation if any exists between Jacques' daily activities and his nightly transformations.

  Solution: Jacques transformed into a squirrel precisely on the days he ate peanuts and did not brush his teeth.
*/

/*
  Parameters:
    @event: an event property from JOURNAL array. Each event contains an array-based listing of things Jacques did that day.
    @journal: journal array from which to gather data (in this case, JOURNAL)
  Return value:
    @table: an array with four indices containing the following data set for each event passed in to tableFor():
      0: event did not occur and Jacques did not transform
      1: event did occur, but Jacques did not transform
      2: event did not occur, but Jacques did transform
      3: event did occur and Jacques did transform
*/
function tableFor(event, journal) {
  var table = [0, 0, 0, 0];
  for (var i = 0; i < journal.length; i++) {
    var entry = journal[i], index = 0;
    if (hasEvent(event, entry)) index += 1;
    if (entry.squirrel) index += 2;
    table[index] += 1;
  }
  return table;
}

/*
  Parameters:
    @event: an event property from JOURNAL array. Each event contains an array-based listing of things Jacques did that day.
    @entry: contains one object from the JOURNAL array, which holds two properties:
      "events": array containing each event Jacques logged in his journal that day
      "squirrel": Boolean value denoting whether Jacques transformed that day
  Return Value:
    true/false: If event shows up in that day's entry, its index in the array exists and does not equal -1, thus returning true. If event does not appear in that entry, indexOf(event) returns -1, and hasEvent() returns false.
*/
function hasEvent(event, entry) {
  return entry.events.indexOf(event) != -1;
}

/*
  PHI COEFFICIENT
    PHI = n(sub11) * n(sub00) - n(sub10) * n(sub01) /
          Math.sqrt(n(1?) * n(0?) * n(?1) * n(?0))
    The notation n(sub01) indicates the number of measurements where the first variable (squirrelness) is false (0) and the second variable (e.g., pizza) is true(1).

    The value n(1?) refers to the sum of all measurements where the first variable (squirrelness) is true. Likewise, n(?0) refers to the sum of the measurements where the second variable (e.g., pizza) is false.
    
  Parameter:
    @table: array-based table or correlation data returned from the tableFor() function.
  Return Value: decimal-based phi coefficient ranging from -1 to 1.
*/
function phi(table) {
  return (table[3] * table[0] - table [2] * table[1]) /
    Math.sqrt((table[2] + table[3]) *
              (table[0] + table[1]) *
              (table[1] + table[3]) *
              (table[0] + table[2]));
}

/*
  Parameter:
    @journal: journal array from which to correlate data. In this case, JOURNAL.
  Return Value:
    @phis: array of objects containing correlation data for each event Jacques logged in his journal. Each object of the array has one event as a key and a decimal number from -1 to 1 as its property (obtained from calling phi() function).
*/
function gatherCorrelations(journal) {
  var phis = [];
  for (var entry = 0; entry < journal.length; entry++) {
    var events = journal[entry].events;
    for (var i = 0; i < events.length; i++) {
      var event = events[i];
      if (!(event in phis))
        phis[event] = phi(tableFor(event, journal));
    }
  }
  return phis;
}

